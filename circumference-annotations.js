function createAnnotation({
  id,
  name,
  unit,
  value,
  view,
  reference,
  description,
  recommendedView,
  labelText,
  labelPosition,
  ringRender,
  labelRender,
  girth3d,
}) {
  return {
    id,
    name,
    unit,
    value,
    view,
    ringPosition: {
      side: girth3d.side,
      reference,
      description,
      recommendedView,
      render: ringRender,
    },
    ringShape: {
      type: "horizontal-loop",
      description: "水平环绕目标部位一周",
    },
    label: {
      text: labelText ?? name,
      position: labelPosition,
      render: labelRender,
    },
    girth3d,
  };
}

